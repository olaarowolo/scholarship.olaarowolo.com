            <p class="mt-8">These Terms and Conditions ("Terms") govern the application for and acceptance of the scholarship fund provided by the Ola Arowolo Scholarship program (the "Program") dedicated to indigenes of Iba Town, Ojo, Lagos, Nigeria. By submitting an application, the Applicant agrees to comply with and be bound by these Terms.</p>

            <hr>

            <!-- 1. Eligibility Criteria -->
            <h2>1. Eligibility Criteria</h2>
            <p>To be eligible for the Program, Applicants must meet all of the following criteria:</p>

            <h3>1.1 Residency and Indigene Status</h3>
            <p>The Applicant must be a verifiable indigene of Iba Town, Ojo, Lagos State, Nigeria. Proof of residency and indigene status, authenticated by local community leaders or designated bodies, is mandatory.</p>

            <h3>1.2 Academic Qualification</h3>
            <p>Applicants must have:</p>
            <ul class="list-disc ml-6 space-y-2">
                <li>Successfully completed secondary education with verifiable WAEC/GCE results that meet the minimum matriculation requirements for a recognized Nigerian University.</li>
                <li>Sat for the Unified Tertiary Matriculation Examination (UTME) administered by JAMB in the current or immediate past academic cycle.</li>
                <li>Secured provisional admission into an accredited full-time university program in Nigeria before final disbursement can be made.</li>
            </ul>

            <h3>1.3 Financial Need</h3>
            <p>While not the sole determinant, demonstration of financial need may be considered during the review process.</p>

            <!-- 2. Application and Selection Process -->
            <h2>2. Application and Selection Process</h2>

            <h3>2.1 Application Submission</h3>
            <p>All applications must be submitted through the official online portal during the published application window. Late submissions will not be considered.</p>

            <h3>2.2 Required Documentation</h3>
            <p>Applicants must upload clear, legible copies of:</p>
            <ul class="list-disc ml-6 space-y-2">
                <li>WAEC/GCE statement of results.</li>
                <li>JAMB result slip.</li>
                <li>Proof of Iba Town indigene status (as stipulated in Section 1.1).</li>
                <li>Passport photograph and means of identification (e.g., National ID, Voter's Card).</li>
            </ul>

            <h3>2.3 Verification</h3>
            <p>Successful candidates will undergo a mandatory physical verification process by the Scholarship Review Committee (SRC) to confirm residency and the authenticity of submitted documents. Any false representation or forged document will result in immediate and permanent disqualification.</p>

            <!-- 3. Funding, Disbursement, and Performance -->
            <h2>3. Funding, Disbursement, and Performance</h2>

            <h3>3.1 Financial Award</h3>
            <p>The Scholarship is a non-renewable annual award up to a maximum amount (currently â‚¦100,000 per academic year). The award is disbursed directly to the Beneficiary's designated bank account upon confirmation of academic status.</p>

            <h3>3.2 Performance-Based Continuation</h3>
            <p>Funding for subsequent academic years is contingent upon the Beneficiary's academic performance.</p>
            <ul class="list-disc ml-6 space-y-2">
                <li>Continuation: Beneficiaries must maintain a minimum Cumulative Grade Point Average (CGPA) of 3.5/5.0 (or equivalent).</li>
                <li>Incentives: Beneficiaries who achieve First Class status (4.5/5.0 or equivalent) may be awarded full subsequent funding (up to the maximum cap).</li>
                <li>Suspension: If a Beneficiary's CGPA falls below the minimum requirement, funding may be suspended for the following year until the standard is met.</li>
            </ul>

            <h3>3.3 Fund Usage</h3>
            <p>The Scholarship fund is intended strictly to cover tuition fees, accommodation, books, and other essential educational expenses.</p>

            <!-- 4. Withdrawal, Termination, and Obligations -->
            <h2>4. Withdrawal, Termination, and Obligations</h2>

            <h3>4.1 Termination of Scholarship</h3>
            <p>The Program reserves the right to terminate the scholarship immediately if a Beneficiary:</p>
            <ul class="list-disc ml-6 space-y-2">
                <li>Is found to have provided false information at any point.</li>
                <li>Changes their course of study without prior written approval from the SRC.</li>
                <li>Is expelled, suspended, or sanctioned by their university for academic dishonesty or misconduct.</li>
            </ul>

            <h3>4.2 Obligation to Report</h3>
            <p>Beneficiaries are obligated to inform the SRC of any change in their academic status, including semester results, change of address, or disciplinary action, within 30 days of the change.</p>

            <h3>4.3 Mentorship and Community</h3>
            <p>All Beneficiaries are expected to actively participate in mentorship sessions and community activities organized by the Program to foster leadership development and give back to Iba Town.</p>

